import { describe, expect, test } from 'bun:test';
import { join } from 'node:path';

// Repo-level invariants for the subsystem-branding website slice
// (subsystem-branding issue 0001): the shared theme package and the apex
// insler.dev site join the workspace as private packages, the apex consumes
// the shared identity rather than carrying its own copy, and both stay
// outside the npm publish and changesets release flows.

const repoRoot = new URL('..', import.meta.url).pathname;
const rootPkg = await Bun.file(join(repoRoot, 'package.json')).json();
const apexPkg = await Bun.file(join(repoRoot, 'packages/website/package.json')).json();
const themePkg = await Bun.file(join(repoRoot, 'packages/website/theme/package.json')).json();

describe('workspace membership', () => {
  test('the apex site is the one explicitly-named non-subsystem workspace entry (ADR-0003)', () => {
    expect(rootPkg.workspaces.packages).toContain('packages/website');
  });

  test('the theme package is matched by the subsystem glob, not a second explicit entry', () => {
    const globs: string[] = rootPkg.workspaces.packages;
    expect(globs.some((g) => new Bun.Glob(g).match('packages/website/theme'))).toBe(true);
    expect(globs).not.toContain('packages/website/theme');
  });
});

describe('release-flow exclusion', () => {
  test.each([
    ['apex', apexPkg],
    ['theme', themePkg],
  ])('the %s package is private and unversioned (outside changesets + npm publish)', (_, pkg) => {
    expect(pkg.private).toBe(true);
    expect(pkg.version).toBeUndefined();
  });

  test('the publish pipeline skips private packages', async () => {
    const ciPublish = await Bun.file(join(repoRoot, 'scripts/ci-publish.sh')).text();
    expect(ciPublish).toContain('"private"');
  });

  test('the shared build config only builds publishable packages', async () => {
    const config = await Bun.file(join(repoRoot, 'tsdown.config.ts')).text();
    expect(config).toContain('discoverBuildableWorkspacePackages');
  });
});

describe('shared theme package', () => {
  test('exposes the family identity surface: tokens, components, family data', () => {
    expect(themePkg.exports['.']).toBe('./src/index.ts');
    expect(themePkg.exports['./tokens.css']).toBe('./src/tokens.css');
    expect(themePkg.exports['./components/*']).toBe('./src/components/*');
  });

  test('the brand tokens are one file (a brand change is one edit)', async () => {
    const tokens = Bun.file(join(repoRoot, 'packages/website/theme/src/tokens.css'));
    expect(await tokens.exists()).toBe(true);
  });
});

describe('apex site', () => {
  test('consumes the shared theme package', () => {
    expect(apexPkg.dependencies['@insler/theme']).toBe('workspace:*');
  });

  test('builds with astro (the build is the test)', () => {
    expect(apexPkg.scripts.build).toBe('astro build');
  });

  test('applies the family identity from the theme, not a local copy', async () => {
    const config = await Bun.file(join(repoRoot, 'packages/website/astro.config.ts')).text();
    expect(config).toContain('@insler/theme');
    expect(config).toContain('familyStarlightConfig');
  });

  test('defines no brand tokens of its own', async () => {
    const apexRoot = join(repoRoot, 'packages/website');
    const glob = new Bun.Glob('src/**/*.{css,astro,mdx,ts}');
    for await (const file of glob.scan(apexRoot)) {
      const text = await Bun.file(join(apexRoot, file)).text();
      // Referencing var(--sl-*) is fine; *defining* --sl-*/--if-* custom
      // properties is the theme's job — that is what makes a brand change
      // one edit.
      expect(text).not.toMatch(/--(?:sl|if)-[\w-]+\s*:/);
    }
  });

  test('homepage describes each subsystem from the shared family data, with links out', async () => {
    const index = await Bun.file(
      join(repoRoot, 'packages/website/src/content/docs/index.mdx')
    ).text();
    expect(index).toContain('SubsystemGrid');
    const grid = await Bun.file(
      join(repoRoot, 'packages/website/src/components/SubsystemGrid.astro')
    ).text();
    expect(grid).toContain("from '@insler/theme'");
    expect(grid).toContain('family.subsystems');
  });
});
